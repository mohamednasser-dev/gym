    {
        $data = WalletTransaction::where('type','payed')->orderBy('id','desc')->get();
        return view('admin.payments.index',compact('data'));
    }

    public function fetch_by_package(Request $request) {
        $data = WalletTransaction::where('type','payed')->where('package_id',$request->backage_id)->orderBy('id','desc')->get();
        return view('admin.payments.index',compact('data'));
